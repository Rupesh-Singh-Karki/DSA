//Function to reverse the queue.
class Solution
{
    void reversequeue(queue<int> &q){
            //base case
            if(q.empty()){
                return;
            }
            
            int num = q.front();
            q.pop();
            reversequeue(q);
            q.push(num);
            
        }
    public:
    queue<int> rev(queue<int> q)
    {
        reversequeue(q);
        return q;
    }
};


